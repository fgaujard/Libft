#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int j;

	i = 0;
	j = ft_strlen(s1);
	while (s1[i] == s2[i] && (s1[i] || s2[i] != '\0'))
		i++;
	return (i == j);
}
