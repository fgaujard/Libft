#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	while (i <= n)
	{
		if (s1[i] == s2[i] && (s1[i] || s2[i] != '\0'))
			i++;
		else
			return (0);
	}
	return (i == n);
}
