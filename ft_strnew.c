#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	int		i;

	i = 0;
	if (!(str = (char *)malloc(sizof(char) * size)))
		return (0);
	while (i < size)
		str[i++] = '\0';
	return (str);
}
